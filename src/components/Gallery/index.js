class Gallery extends Component {
  state = {
    selectedImageId: 0,
  }

  handleThumbnailClick = id => {
    this.setState({selectedImageId: id})
  }

  render() {
    const {selectedImageId} = this.state

    const selectedImage = imagesList.find(image => image.id === selectedImageId)

    return (
      <div className="app-container">
        <div className="gallery-container">
          <div className="main-image-container">
            <img
              src={selectedImage.imageUrl}
              alt={selectedImage.imageAltText}
              className="main-image"
            />
            <h1 className="image-description">Nature Photography</h1>
            <p className="para">Nature Photography by Rahul</p>
          </div>
          <ul className="thumbnails-list">
            {imagesList.map(image => (
              <ThumbnailItem
                key={image.id}
                imageDetails={image}
                isSelected={image.id === selectedImageId}
                handleThumbnailClick={this.handleThumbnailClick}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}

export default Gallery
